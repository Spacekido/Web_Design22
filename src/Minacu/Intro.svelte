<div class="mdc-typography--headline2 uppercase">Minaçu, Brasile</div>

<div class="left">
  <div class="mdc-typography--headline4 uppercase">Una storia controversa</div>
  <div class="mdc-typography--body1">
    Minaçu è un comune brasiliano dello stato del Goiàs dove opera la SAMA, un'industria franco-brasiliana dedita all’estrazione di amianto crisotilo, e dove è
    presente la più grande miniera di amianto del Brasile chiamata Cana Brava. L'amianto ha reso Minaçu uno dei comuni più ricchi del paese.
    <br /><br />
    Le leggi federali in Brasile attestano chiaramente la pericolosità dell’amianto. Ciononostante l’estrazione, la manipolazione e la commercializzazione di quest’ultimo
    son rimaste attive fino al 2017, anno in cui la Corte Suprema Federale ha vietato l’uso del minerale.
    <br /><br />
    Nel 2019, peró, il governatore del Brasile ha emanato una legislazione nello stato brasiliano di Goiás, approvando l’estrazione ed esportazione di amianto, facendo
    continuare così l’operato di Cana Brava. Questa mossa politica ha portato gli abitanti di Minaçu di fronte a un bivio: caduta in un periodo economico difficile,
    in qualsiasi momento una decisione del tribunale potrebbe interrompere l'attività mineraria della cittá.
  </div>
</div>

<div class="right">
  <div class="mdc-typography--headline4 uppercase primary">L'opinione degli abitanti</div>
  <div class="mdc-typography--body1">
    Alcuni residenti desiderano che la città abbandoni l’amianto e si rinnovi, secondo altri invece senza amianto la città è finita. "Se Sama si ferma, la città
    si ferma", ha detto Joaquim de Souza, 54 anni, operaio della SAMA, che vive vicino alla massiccia collina di detriti di amianto di Minaçu.
    <br /><br />
    Molti si riferiscono all'azienda come la "madre di Minaçu, che si prende cura dei suoi abitanti, ma in realtá è una madre perversa che fa ammalare le persone,
    voltandogli poi le spalle".
    <br /><br />
  </div>
</div>

